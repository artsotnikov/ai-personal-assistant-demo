async function migrate() {
    console.log('📝 Adding system prompts to AI configs...\n');

    for (const [taskType, prompt] of Object.entries(PROMPTS)) {
        try {
            const result = await db.update(aiModelConfigs)
                .set({
                    systemPrompt: prompt,
                    updatedAt: new Date()
                })
                .where(eq(aiModelConfigs.taskType, taskType as any))
                .returning();

            if (result.length > 0) {
                console.log(`✅ ${taskType}: добавлен промпт (${prompt.length} chars)`);
            } else {
                console.log(`⚠️  ${taskType}: конфиг не найден в БД`);
            }
        } catch (error) {
            console.error(`❌ ${taskType}: ${error}`);
        }
    }

    console.log('\n✨ Migration complete!');
    process.exit(0);
}

migrate();
