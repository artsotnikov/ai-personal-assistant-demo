/**
 * Собирает финальный системный промпт агента.
 * 
 * Приоритет: persona из БД (если есть) > persona из кода (fallback)
 * Workflow-инструкции ВСЕГДА из этого файла.
 * 
 * @param dbPrompt - systemPrompt из БД (из aiConfigService)
 * @param codePrompt - константа *_SYSTEM_PROMPT из файла агента
 * @param contextSection - отформатированный контекст памяти
 */
export function buildAgentSystemPrompt(
    dbPrompt: string | null,
    codePrompt: string,
    contextSection: string,
): string {
    // Persona: приоритет у БД (позволяет менять через UI), fallback — код
    const persona = dbPrompt && dbPrompt.trim().length > 0
        ? dbPrompt
        : codePrompt;

    return `${persona}

${TOOL_WORKFLOW_PROMPT}

## Контекст пользователя:
${contextSection || "Контекст пока не накоплен."}`;
}
