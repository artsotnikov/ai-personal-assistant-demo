// Если запускается как скрипт
async function runMigration() {
    const API_URL = process.env.API_URL || 'http://localhost:5000';

    console.log('🚀 Начинаем миграцию промптов агентов...\n');

    for (const [taskType, systemPrompt] of Object.entries(PROMPTS)) {
        try {
            const response = await fetch(`${API_URL}/api/ai-configs/${taskType}`, {
                method: 'PUT',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({ systemPrompt }),
            });

            if (response.ok) {
                console.log(`✅ ${taskType}: промпт обновлён`);
            } else {
                const error = await response.text();
                console.error(`❌ ${taskType}: ${error}`);
            }
        } catch (err) {
            console.error(`❌ ${taskType}: ${err}`);
        }
    }

    console.log('\n✨ Миграция завершена!');
    console.log('Проверьте результат: http://localhost:5000/settings/ai');
}

// Экспорт для использования в других модулях
export { PROMPTS };

// Авто-запуск при прямом вызове
runMigration().catch(console.error);
